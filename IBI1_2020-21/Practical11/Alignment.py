#create a function to calculate the alignment score
def we(seq1,seq2):
    edit_distance = 0
    for i in range (len(seq1)):
        if seq1[i] != '\n':
            d = amino.index(seq1[i])
            e = amino.index(seq2[i])
            edit_distance = edit_distance + blosum[d][e]
    return edit_distance

#create a function to calculate the percentage of identical amino acids
def per(seq1,seq2):
    j = 0
    for i in range (len(seq1)):
        if seq1[i] == seq2 [i]:
            j = j + 1
    per = j / len(seq1) *100
    return per

